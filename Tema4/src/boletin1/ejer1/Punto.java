package boletin1.ejer1;

public class Punto { // Representa un punto de dos dimensiones en un plano
	int x; // Coordenada x
	int y; // Coordenada y
	
	/**
	 * Constructor que crea objeto punto de dos dimensiones en un plano
	 * @param x coordenada x
	 * @param y coordenada y
	 */
	Punto(int x,int y){
		this.x = x;
		this.y = y;
	}
}
