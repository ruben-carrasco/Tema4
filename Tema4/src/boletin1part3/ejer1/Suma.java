package boletin1part3.ejer1;

public class Suma {
	/**
	 * Metodo que suma dos numeros enteros
	 * 
	 * @param num1 Primer numero
	 * @param num2 Segundo numero
	 * @return suma La suma de ambos
	 */
	public static int suma(int num1, int num2) {
		// Almacena la suma
		int suma = 0;

		// Sumamos y almacenamos en variable
		suma = num1 + num2;

		// Devolvemos la suma
		return suma;
	}

	/**
	 * Metodo que suma dos numeros con decimales
	 * 
	 * @param num1 Primer numero
	 * @param num2 Segundo numero
	 * @return suma La suma de ambos
	 */
	public static double suma(double num1, double num2) {
		// Almacena la suma
		double suma = 0;

		// Sumamos y almacenamos en variable
		suma = num1 + num2;

		// Devolvemos la suma
		return suma;

	}
}
